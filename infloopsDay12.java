public class infloops {

    public static void main(String[] args) {
       
        System.out.println("For Loop Infinite Journey:");
        for (int i = 0; i >= 0; i++) {
                       System.out.println("Loop iteration: " + i);
        }
        
        
        System.out.println("\nWhile Loop Infinite Journey:");
        int j = 0;
        while (true) {
          
            System.out.println("Loop iteration: " + j);
            j++;
        }
    }
}
